CREATE TABLE Human_Telomere_Raw (
	 ID INT NOT NULL,
	 HHID INT NOT NULL,
     PD INT NOT NULL,
     PLATE FLOAT NOT NULL,
     WELL VARCHAR NOT NULL,
     FINAL_T_S FLOAT NOT NULL,
	 FINAL_CV FLOAT NOT NULL,
     NOTES VARCHAR,
     FOREIGN KEY (ID) REFERENCES Human_Bio_Raw (ID),
     PRIMARY KEY (ID)
);

